def gen_yomikata():
    """漢字読み — generate from vocabulary list."""
    questions = []
    templates = [
        "この<u>{}</u>は重要な問題だ。",
        "彼は<u>{}</u>の態度を示した。",
        "この<u>{}</u>な状況を改善すべきだ。",
        "両者の意見には<u>{}</u>がある。",
        "その<u>{}</u>は避けられない。",
        "<u>{}</u>を感じる場面だった。",
        "彼女は<u>{}</u>に満ちた表情だった。",
        "事態は<u>{}</u>の一途をたどった。",
        "次の<u>{}</u>について説明してください。",
        "この<u>{}</u>が問題の核心だ。",
    ]
    for word, reading, meaning in N1_WORDS:
        for _ in range(4):  # 4 variants per word
            t = random.choice(templates)
            sentence = t.format(word)
            wrong_pool = [x for x in N1_WORDS if x[1] != reading]
            wrong = [r for _, r, _ in random.sample(wrong_pool, min(3, len(wrong_pool)))]
            opts = [reading] + wrong[:3]
            random.shuffle(opts)
            ans = opts.index(reading)
            questions.append({
                "section": "vocab", "q_type": "yomikata",
                "question_text": sentence,
                "options": json.dumps(opts, ensure_ascii=False),
                "answer": ans,
                "explanation": f"{word}（{reading}）：{meaning}。"
            })
    # Also generate from the extended vocabulary for more variety
    extra_context = [
        ("葛藤", "かっとう", "纠葛"), ("偽造", "ぎぞう", "伪造"), ("誇張", "こちょう", "夸张"),
        ("懲役",  "ちょうえき", "徒刑"), ("猶予", "ゆうよ", "延缓"), ("賠償", "ばいしょう", "赔偿"),
        ("詐欺", "さぎ", "欺诈"), ("横領", "おうりょう", "贪污"), ("是正", "ぜせい", "纠正"),
    ]
    for word, reading, meaning in extra_context:
        t = random.choice(templates)
        sentence = t.format(word)
        wrong_pool = [x for x in N1_WORDS if x[1] != reading]
        wrong = [r for _, r, _ in random.sample(wrong_pool, min(3, len(wrong_pool)))]
        opts = [reading] + wrong[:3]
        random.shuffle(opts)
        ans = opts.index(reading)
        questions.append({
            "section": "vocab", "q_type": "yomikata",
            "question_text": sentence,
            "options": json.dumps(opts, ensure_ascii=False),
            "answer": ans,
            "explanation": f"{word}（{reading}）：{meaning}。"
        })
    return questions


