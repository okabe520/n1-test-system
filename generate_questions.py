def gen_grammar_form():
    """文法形式判断."""
    questions = []
    sentences = [
        "彼の努力には感動（　）。",
        "こんな結果は奇跡（　）。",
        "彼女の才能には驚き（　）。",
        "それは賞賛（　）行為だ。",
        "困難（　）前に進んだ。",
        "練習（　）上達はない。",
        "彼は帰国（　）連絡がない。",
        "結果（　）過程も重要だ。",
        "彼は努力の（　）成功した。",
        "今回の失敗は若さ（　）ものだ。",
        "その案（　）賛成だ。",
        "彼の行動は理解（　）。",
        "社長（　）決断すべきだ。",
        "一歩（　）間違えば大惨事だ。",
        "誰（　）間違いはある。",
        "チャンス（　）逃すな。",
        "データ（　）判断した。",
        "失敗（　）成長がある。",
        "これ（　）議論は無意味だ。",
        "本番（　）練習あるのみ。",
    ]
    for i, pattern in enumerate(GRAMMAR_PATTERNS):
        for _ in range(2):  # 2 variants
            pat_text, meaning = pattern
            s = random.choice(sentences)
            others = random.sample([p for p in GRAMMAR_PATTERNS if p != pattern], 3)
            opts = [pat_text] + [o[0] for o in others]
            random.shuffle(opts)
            ans = opts.index(pat_text)
            questions.append({
                "section": "grammar", "q_type": "form",
                "question_text": s,
                "options": json.dumps(opts, ensure_ascii=False),
                "answer": ans,
                "explanation": f"{pat_text}：{meaning}。"
            })
    return questions


